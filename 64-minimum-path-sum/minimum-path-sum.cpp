class Solution {
public:
   
    int minPathSum(vector<vector<int>>& grid) {
        int m=grid.size();
        int n=grid[0].size();
        if(m==1 && n==1) return grid[0][0];
        vector<vector<int>> dp(m,vector<int>(n,-1));

        for(int i=0;i<m;i++){
            for(int j=0;j<n;j++){
                if(i==0 && j==0) {
                    dp[i][j]=grid[i][j];
                    continue;
                }
                int up=1e9;
                int left=1e9;
                if(i>0){
                    up=dp[i-1][j];
                }
                if(j>0){
                    left=dp[i][j-1];
                }
                dp[i][j]=min(up,left)+grid[i][j];
            }
        }

         
        return dp[m-1][n-1];
    }
};